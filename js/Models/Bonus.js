// Classe Bonus - Bonus aléatoire généré sur le terrain
class Bonus {
    /**
     *
     * @param {string} name
     * @param {string} type
     * @param {number} amount
     * @param {string} image
     */
    constructor(name, type, amount, image){
        this.name = name;
        this.type = type;
        this.amount = amount;
        this.image = image;
    }

}
export { Bonus }
